function ocultar1(){

    document.getElementById("rojo").style.backgroundColor="transparent";
};

function mostrar1(){

    document.getElementById("rojo").style.backgroundColor="red";
};


function ambos1()
{

    if(document.getElementById("rojo").style.backgroundColor=="transparent")
    {
        document.getElementById("rojo").style.backgroundColor="red"
    }
    else
    {
        document.getElementById("rojo").style.backgroundColor="transparent"
    }

}


